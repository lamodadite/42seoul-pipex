#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	if (s == NULL)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	return (1);
}
